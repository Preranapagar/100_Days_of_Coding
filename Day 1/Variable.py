#Creating Variable

No = 15 #dynamic typing #int

print(No)
print(type(No))

No = 14.5 #float

print(No)
print(type(No))

No = "Python" #string

print(No)
print(type(No))

No = True #boolean

print(No)
print(type(No))

No = 4+2j #complex

print(No)
print(type(No))

#Numeric Datatypes : Int, float, boolean, complex 
